package the.testclass;//定义包

//一个公有的empp类
public class Empp {
    //公有fakeyou为双精度浮点类型
    public double fakeyou;
    //公有的fakeyou为双精度浮点类型
    public double fakeyou(Returns returns){
        //双精度浮点类型meke等于零
        double meke = 0;
        //meke等于the乘12
        meke = returns.getThe()* 12;
        //返回meke
        return meke;
    }
    //公有的mxp为双精度浮点类型
    public double mxp(Returns returns){
        //双精度浮点类型emp等于零
        double emm = 0;
        //条件the小于10000
        if (returns.getThe()<10000){
            //emm等于60
            emm = 60;
        }else{
            //不小于emm就等于80
            emm = 80;
        }
        //返回emm
        return emm;
    }
}
